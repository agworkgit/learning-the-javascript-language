let string1 = "this is a string"; // string literal
console.log(string1);

let string2 = "12"; // still a string
console.log(string2);

let myString = "This is my string. Leave it alone";
lcString = myString.toLocaleLowerCase();
console.log(lcString); // this is my string. leave it alone

let tlString = `I don't have to "worry" about quotes of any kind.`; // this is a template literal
console.log(tlString); // I don't have to "worry" about quotes of any kind.

let myString2 = "Don't touch this!";
let declaration = `I told you before. ${myString2}`;
console.log(declaration); // I told you before. Don't touch this!


